      <h1 class="productHeading"><u>Essex_EGP-173:</u></h1>

      <hr>

      <p>Seit der Einf&uuml;hrung der Instrumente von Essex &#8222;designed by Steinway &amp; Sons&quot; im Jahre 2006 ist die Steinway Familie komplett. Dank der Verarbeitung ausgew&auml;hlter Materialien in Verbindung mit modernsten Herstellungsverfahren wurden erstmals Fl&uuml;gel und Klaviere konstruiert, die einen hohen Musikgenuss zu einem attraktiven Preis-/Leistungsverh&auml;ltnis bieten. Erneut machten sich Steinway Spezialisten an die Arbeit und entwickelten Instrumente, die &uuml;ber die neuesten Konstruktionsmerkmale und eine Reihe von Steinway Patenten verf&uuml;gen.</p>

      <p>In Zusammenarbeit mit dem renommierten M&ouml;beldesigner William Faber realisierte Steinway &amp; Sons hervorragende Klaviere und Fl&uuml;gel in unterschiedlichen Stilrichtungen und Furnieren zu einem vern&uuml;nftigen Preis. Mit ihrem eleganten Design und den verschiedenen Ausf&uuml;hrungen sind diese Instrumente eine Bereicherung f&uuml;r Ihr Zuhause.</p>

      <p>Alle Essex Instrumente vereinen die neuesten Konstruktionsmerkmale und Produktionsvorgaben des Forschungs- und Entwicklungsteams von Steinway &amp; Sons. Dank der Verarbeitung hochwertiger Materialien in Verbindung mit modernsten Herstellungsverfahren und sorgf&auml;ltig ausgew&auml;hlter maschineller Fertigung bieten die Instrumente von Essex &#8222;designed by Steinway &amp; Sons&ldquo; uneingeschr&auml;nkten Musikgenuss zu einem bislang unerreichten Preis-/Leistungsverh&auml;ltnis.</p>

      <p>Der Resonanzboden (Steinway Design) ist zu den Enden hin verj&uuml;ngt und kann freier schwingen und dadurch mehr Tonf&uuml;lle erzeugen. Die Mechanik enth&auml;lt keine Plastik-Bauteile, sondern ist ausnahmslos aus Holz gefertigt. Sie zeigt die erforderliche Stabilit&auml;t, eine schnelle Repetitionsf&auml;higkeit und durch die besondere Hebelgeometrie der Hebelglieder ein sensibles Ansprechverhalten. Als weitere Steinway Eigenschaften bzw. Designs sind die Rasten und Spreizen, der Stimmstock sowie die Stimmwirbel, Saiten und Gussplatte in die Konstruktion mit eingeflossen.</p>

